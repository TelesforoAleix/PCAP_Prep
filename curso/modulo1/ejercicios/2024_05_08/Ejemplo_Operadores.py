# Tipos de operadores aritméticos:
    # **    Exponente
    # *     Multiplicación
    # /     Division
    # //    División entera
    # %     Módulo o resto
    # +     Suma
    # -     Resta

# Las operaciones siempre van de izquierda a derecha y siguien con las reglas de prioridades de las matemáticas.

# Operadores relacionales
# Siempre devuelven valores booleanos
    # >     Mayor que
    # >     Menos que
    # >=    Mayor o igual que
    # <=    Menos or igual que
    # !=    No igual a
    # ==    Igual a