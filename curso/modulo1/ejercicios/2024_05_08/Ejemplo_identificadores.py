# Python maneja literales y variables
# Tipos de literales:
    # - Numericos: (int, float, complex)
    # - Booleanos: True, False
    # - Texto: "Aleix", 'Barcelona'
    # - Colecciones: (list, set, tuple, dict)
    # - Objetos

# Identificadores, se utilizan para definir los nombres de las variables, funciones y clases

# Identificadores válidos: deben de empezar por letra (alfabeto inglés) o guión bajo(_) seguido de letras o números o guiones.

edad = 27                   # Variable numérica entera
altura = 1.89               # Variable numérica real
complejo = 4.8 + 2.4        # Variable numérica compleja
mayor = True                # Variable booleana
nombre = 'Aleix'            # Variable de texto o cadena
numeros = [2,3,4]           # Variable tipo de colección lista

# Sintaxis de escritura de los identificadores:
    # Python es Case Sensitive, distingue entre mayúsculas y minúsculas.
    
# - Snake Case: Se utilizan para los identificadores de variables y funciones, y todas las letras deben de estar en minúsculas
    # Ejemplo: edad_maxima, peso_altura

# - Camel Case: Se utiliza para los identificadores de clases, la primera letra del identificador irá en mayusculas y después minúsculas.
    # Ejemplo: DataFile, DataTemp

# Las variables que nunca cambian de valores (o constantes) siempre se escriben en mayúsculas.
